def all_odd(numbers):
    """Return a list of only the odd numbers in the input list.

    For example::

        >>> all_odd([1, 2, 7, -5])
        [1, 7, -5]

        >>> all_odd([2, -6, 8])
        []
    """

    # initializing an empty list to append odd numbers to
    odd_numbers = []

    for number in numbers:
        # if the remainder of the number modulo 2 is 1, that means it's
        # an odd number
        if number % 2 == 1:
            odd_numbers.append(number)

    return odd_numbers


